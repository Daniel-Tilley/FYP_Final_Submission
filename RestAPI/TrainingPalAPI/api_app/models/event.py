class Event:
    def __init__(self, Id, Type, Name, Host_Username, Created_Date, Event_Date, Attendees):
        self.Id = Id
        self.Type = Type
        self.Name = Name
        self.Host_Username = Host_Username
        self.Created_Date = Created_Date
        self.Event_Date = Event_Date
        self.Attendees = Attendees
