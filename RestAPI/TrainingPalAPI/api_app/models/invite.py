class Invite:
    def __init__(self, Id, Invite_Type, Sent_By, Sent_To, Status, Send_Date, Event_Id):
        self.Id = Id
        self.Invite_Type = Invite_Type
        self.Sent_By = Sent_By
        self.Sent_To = Sent_To
        self.Status = Status
        self.Send_Date = Send_Date
        self.Event_Id = Event_Id